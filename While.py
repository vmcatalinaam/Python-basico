

# WHILE

# Tabla de multiplicar

tabla = int(input("Que tabla desea calcular?: "))

# se crea variable contador

contador = 1

print(f"Tabla del {tabla}")
# repeticion

while (contador < 11): #que cuente menor que 10, como contador ya posee 1, va hasta 11
    resultado = tabla * contador
    # Incrementamos el contador
    contador = contador+1 #el contador en cada pasada se incrementa 1, para comprbar y seguir ejecutandose.
    #mostrar la tabla

    # comprbamos s vale 4 y salimos
    if contador == 4:
        print("El contador es igual a 4 y no continua")
        break
    
    print(f'{tabla} por {contador - 1} es igual a {resultado}\n') 
print("Exit")
